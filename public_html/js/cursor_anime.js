$( ".cell1" ).mouseenter(function(){ 	   
	   anime({
  targets: '.cursor_anime .cursor_el', 
  translateY: anime.stagger(1, {grid: [10, 10], from: 'center', axis: 'y'}), 
  delay: anime.stagger(10, {grid: [10, 10], from: 'center'}),
  easing: 'easeOutElastic'
});   
	   
});
	  
	  
$( ".cell1" ).mouseleave(function(){ 
	   
	   anime({
  targets: '.cursor_anime .cursor_el',
  translateY: anime.stagger(0, {grid: [10, 10], from: 'center', axis: 'y'}), 
  delay: anime.stagger(40, {grid: [10, 10], from: 'center'}),
  easing: 'easeOutElastic'
});   
	   
	   
	   
});


